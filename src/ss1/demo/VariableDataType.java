package ss1.demo;
public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;
        float f = 3.04f;
        double d = 3.5;
        boolean b = true;
        char c = 'a';
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
    }
}

